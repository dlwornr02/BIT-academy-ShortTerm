#include<stdio.h> //헤더파일을 포함(참조)하는것을 선언
void main()//메인함수 : 프로그램의 시작과 끝 , os가 실행시켜줌
{ //main함수 정의부분
	int n = 100; //n이라는 이름의 4Byte 정수형을 선언, n은 변경가능한 메모리의 이름(변수),100은 n의 value라고 한다.
	printf("int : %d\n",100); //100은 상수 정의하지 않았기때문에 자동으로 int형으로 지정된다 
	printf("int : %d\n",n); //printf 사용한다. -> 호출한다.
}
